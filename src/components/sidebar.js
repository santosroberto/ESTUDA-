export class Sidebar {
  constructor({ onNavigate } = {}) {
    this.element = null
    this.callbacks = { onNavigate }
    this._links = []
    this._activeRoute = null
  }

  render() {
    this.element = document.createElement('nav')
    this.element.className = 'sidebar'

    const logo = document.createElement('div')
    logo.className = 'sidebar__logo'
    logo.innerHTML = `
      <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round">
        <path d="M12 2L2 7l10 5 10-5-10-5z"/>
        <path d="M2 17l10 5 10-5"/>
        <path d="M2 12l10 5 10-5"/>
      </svg>
      <span>Estuda+</span>
    `
    this.element.appendChild(logo)

    this._navEl = document.createElement('div')
    this._navEl.className = 'sidebar__nav'
    this.element.appendChild(this._navEl)

    this._buildNav()

    return this.element
  }

  _buildNav() {
    this._navEl.innerHTML = ''
    this._links = []

    navItems.forEach((item) => {
      const link = document.createElement('a')
      link.className = 'sidebar__link'
      link.href = `#${item.route}`
      link.innerHTML = `
        <span class="sidebar__link-icon">${item.icon}</span>
        <span>${item.label}</span>
      `

      link.addEventListener('click', (e) => {
        e.preventDefault()
        if (this.callbacks.onNavigate) {
          this.callbacks.onNavigate(item.route)
        }
      })

      this._navEl.appendChild(link)
      this._links.push({ el: link, route: item.route })
    })
  }

  setActiveRoute(route) {
    this._activeRoute = route

    this._links.forEach(({ el, route: linkRoute }) => {
      const isActive = linkRoute === route
        || (linkRoute !== '/' && route.startsWith(linkRoute))

      el.classList.toggle('sidebar__link--active', isActive)
    })
  }

  toggle() {
    if (this.element) {
      this.element.classList.toggle('app-shell__sidebar--open')
    }
  }

  destroy() {
    this.element = null
    this._links = []
    this._navEl = null
  }
}
